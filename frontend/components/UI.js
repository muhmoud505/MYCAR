export function StatusBadge({ status, children }) {
  const statusClasses = {
    active: 'badge-success',
    pending: 'badge-warning',
    inactive: 'badge-secondary',
    sold: 'badge-error',
    new: 'badge-primary'
  }

  return (
    <span className={`badge ${statusClasses[status] || 'badge-secondary'}`}>
      {children}
    </span>
  )
}

export function Avatar({ src, alt, name, size = 'md', className = '' }) {
  const sizeClasses = {
    sm: 'w-6 h-6 text-xs',
    md: 'w-8 h-8 text-sm',
    lg: 'w-10 h-10 text-base',
    xl: 'w-12 h-12 text-lg',
    '2xl': 'w-16 h-16 text-xl'
  }

  return (
    <div className={`relative inline-flex items-center justify-center ${sizeClasses[size]} rounded-full bg-primary-100 text-primary-600 font-medium ${className}`}>
      {src ? (
        <img src={src} alt={alt} className="w-full h-full rounded-full object-cover" />
      ) : (
        <span>{name?.charAt(0) || 'U'}</span>
      )}
    </div>
  )
}

